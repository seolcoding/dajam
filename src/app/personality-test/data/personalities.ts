// Helper function to get all types by category
export function getTypesByCategory(category: 'NT' | 'NF' | 'SJ' | 'SP'): PersonalityType[] {
  const categoryMap = {
    NT: ['INTJ', 'INTP', 'ENTJ', 'ENTP'],
    NF: ['INFJ', 'INFP', 'ENFJ', 'ENFP'],
    SJ: ['ISTJ', 'ISFJ', 'ESTJ', 'ESFJ'],
    SP: ['ISTP', 'ISFP', 'ESTP', 'ESFP'],
  };

  return categoryMap[category].map(code => PERSONALITY_TYPES[code as PersonalityCode]);
}
